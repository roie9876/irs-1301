def _normalize_code(code: str) -> str:
    normalized = code.strip().replace("field_", "")
    return normalized


@lru_cache(maxsize=1)
def _load_field_index() -> dict[str, dict]:
    schema = json.loads(SCHEMA_PATH.read_text(encoding="utf-8"))
    index: dict[str, dict] = {}

    for part in schema.get("form_parts", []):
        for section in part.get("sections", []):
            fields = section.get("fields", [])
            for field_group in fields:
                for code in str(field_group).split("/"):
                    normalized_code = _normalize_code(code)
                    if not normalized_code or normalized_code == "---":
                        continue
                    index[normalized_code] = {
                        "code": normalized_code,
                        "title": section.get("name_he") or part.get("part_name_he") or normalized_code,
                        "description": section.get("notes") or part.get("description") or "",
                        "part_id": part.get("part_id", ""),
                        "part_name_he": part.get("part_name_he", ""),
                        "section_num": section.get("section_num"),
                        "section_name_he": section.get("name_he", ""),
                        "guide_line": section.get("guide_line") or part.get("guide_line"),
                        "tax_rate": section.get("tax_rate", ""),
                        "notes": [
                            item
                            for item in [section.get("notes"), part.get("notes")]
                            if isinstance(item, str) and item
                        ],
                    }

    # Apply all overrides (from any part)
    for code, override in FIELD_OVERRIDES.items():
        index[code] = {
            "code": code,
            "section_num": None,
            "section_name_he": "",
            "tax_rate": "",
            **override,
        }

    return index


def get_field_help(code: str) -> dict:
    normalized_code = _normalize_code(code)
    field = _load_field_index().get(normalized_code)
    if not field:
        raise HTTPException(status_code=404, detail="לא נמצא הסבר עבור שדה זה")

    description = field["description"]
    if isinstance(description, list):
        description = description[0] if description else ""

    notes = field.get("notes", [])
    if not isinstance(notes, list):
        notes = [notes] if notes else []

    return {
        **field,
        "description": description or "אין עדיין הסבר מפורט לשדה זה.",
        "notes": notes,
    }
